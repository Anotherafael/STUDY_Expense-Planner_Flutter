import 'package:flutter/material.dart';

/// It's a class that contains static methods that return a color
class AppColors {
  static Color get primaryColor => const Color(0xFF36BFB1);
  static Color get secondaryColor => const Color(0xFF038C73);
  static Color get tertiaryColor => const Color(0xFF02735E);
  static Color get darkColor => const Color(0xFF0D0D0D);
  static Color get darkColorAlt => const Color(0xFF014034);
  static Color get black => const Color.fromARGB(255, 0, 0, 0);
  static Color get white => const Color.fromARGB(255, 255, 255, 255);
  static Color get grey => Colors.grey;
}
